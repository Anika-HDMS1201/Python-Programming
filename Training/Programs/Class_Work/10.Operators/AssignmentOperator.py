# Assignment operators are used to assign values to variables:
x = 10
print(x) # 10

x += 3 # x = x + 3
print(x) # 13

x -= 3
print(x) # 10

x *= 3
print(x) # 30

x /= 3
print(x) # 10.0

x %= 3
print(x) # 1.0

x //= 3
print(x) # 0.0

x **= 3
print(x) # 0.0

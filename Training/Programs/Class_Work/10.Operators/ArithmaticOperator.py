#Arithmetic operators are used with numeric values to perform common mathematical operations:
x = 15
y = 4

print(x + y) #19
print(x - y) #11
print(x * y) #60
print(x / y) #3.75
print(x % y) #3 {Modulus -> returns integer reminder}
print(x ** y) #50,625 {Exponential -> returns power value}
print(x // y) #3 {Floor Division -> Returns integer as quotent}